import java.util.Date;

public class TasksTable {
	String username;
	Date date;
	String time;
	String task;
	
	public TasksTable(String username, Date date, String time, String task)
	{
		this.username = username;
		this.date = date;
		this.time = time;
		this.task = task;
	}
	
	
}
